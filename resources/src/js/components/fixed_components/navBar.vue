<template>
    <nav class="navbar">
        <back-button></back-button>
        <div v-if="!checkIfLogin">
            <div v-if="this.displayCurrentTerm != null" class="navbar__header">
            {{this.displayCurrentTerm}}
            </div>
            <a data-interactable :href="this.url + '/support'" class="feedback-button pull-right">Feedback</a>
        </div>
    </nav>
</template>

<script>
import { mapGetters } from 'vuex';
import backButton from './backButton.vue';

export default {
  data() {
    return {
      url: '',
    };
  },
  components: {
    backButton,
  },
  created() {
    this.url = document.querySelector('meta[name=app-url]').content;
  },
  computed: {
    ...mapGetters(['term']),

    displayCurrentTerm() {
      if (this.term != null) {
        const termCode = this.term;
        switch (termCode.charAt(3)) {
          case '3':
            this.displayedTerm = 'Spring';
            break;
          case '5':
            this.displayedTerm = 'Summer';
            break;
          case '7':
            this.displayedTerm = 'Fall';
            break;
          case '9':
            this.displayedTerm = 'Winter';
        }
        if (termCode.charAt(0) == '2') {
          this.displayedTerm
						+= ` ${
						 termCode.charAt(0)
						 }0${
						 termCode.substring(1, 3)}`;
        } else {
          this.displayedTerm
						+= ` ${
						 termCode.charAt(0)
						 }9${
						 termCode.substring(1, 3)}`;
        }
        return this.displayedTerm;
      }
    },
    checkIfLogin() {
      if (document.getElementById('loginContainer')) { return true; }
      return false;
    },
  },
};
</script>
